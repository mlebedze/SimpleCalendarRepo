namespace SimpleCalendar
{
    /// <summary>
    /// A location is the geographical venue for an event.</summary>
    public class Location
    {
        /// <summary>
        /// The name of the location.</summary>
        public string Name;

        /// <summary>
        /// The latitudinal coordinate of the location.</summary>
        public float Latitude;

        /// <summary>
        /// The longitudinal coordinate of the location.</summary>
        public float Longitude;
    }
}
